# Math Functions()

x = 3.14
y = -4
z = 5

round_function = round(x)
absolute_function = abs(y)
power_function = pow(z, 4)
max_function = max(x, y, z)
min_function = min(x, y, z)

print(round_function) # 3
print(absolute_function) # 4
print(power_function) # 625
print(max_function) # 5
print(min_function) # -4